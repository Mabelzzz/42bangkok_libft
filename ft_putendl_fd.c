#include "libft.h"

void	ft_putendl_fd(char *str, int fd)
{
	int	s;

	if (!str)
		return ;
	s = 0;
	while (str[s] != '\0')
	{
		write(fd, &str[s], 1);
		s++;
	}
	write(fd, "\n", 1);
}
